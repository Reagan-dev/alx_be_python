# calculate user's age in a future year
# read the current age
current_age = int(input("How old are you? "))


# read the current year
current_year = 2023


# read the future year
future_year = 2050


# calculate the future age
future_age = current_age + (future_year - current_year)


# give the user the output
print("In", future_year, ",", "you will be", future_age, "years old.")
